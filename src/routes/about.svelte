    <p>J'ai débuté dans la vie active comme technicien en télécommunications dans le privé. Le secteur étant rapidement bouché, j'ai dû m'orienter vers une branche sans rapport avec ma formation initiale, mais où le travail ne manquait pas. J'officiais de nuit dans la sécurité, en statu quo durant de longues années, me demandant si j'étais encore apte à un travail constructif et plaisant.</p>
    <p>Mes vacations nocturnes me permirent de m'enrichir intellectuellement.</p>
    <p>Puis, en cherchant un moyen d'automatiser les tâches les plus courantes, je redécouvris l'informatique grâce à GNU/Linux, bash, le codage python...</p>
    <p>
      En réalisant la puissance du codage, je ne fus pas long à décider de suivre une longue et fastidieuse (mais Ô combien passionnante) formation de développement web, option FrontEnd, avant de m'essayer au Backend. Déçu par PHP (notamment à cause de certaines faiblesses), je m'orientai vers le
      <b><i>tout JavaScript</i></b>. NodeJS est prévu pour plus tard.
    </p>
    <p>Convaincu par la supériorité <i>sine qua non</i> de Linux, je testai des distributions de diverses familles: Ubuntu, Mint, Debian, MX, Manjaro, pour enfin me fixer avec Fedora.</p>
    <p>J'adoptai également Mac fin 2019, pour pouvoir tirer parti de sa puissance et sa fluidité.</p>
    <p>
